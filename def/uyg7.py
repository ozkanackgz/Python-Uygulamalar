
sayi1=int(input("Birinci sayıyı giriniz="))
sayi2=int(input("İkinci sayıyı giriniz="))

def toplama():
    sonuct=sayi1+sayi2
    print("Toplam: ",sonuct)

def cikarma():
    sonucc=sayi1-sayi2
    print("Çıkarma: ",sonucc)

def carpma():
    sonucx=sayi1*sayi2
    print("Çarpım: ",sonucx)

def bolum():
    sonucb=sayi1/sayi2
    print("Bölüm: ",sonucb)

toplama()
cikarma()
carpma()
bolum()
