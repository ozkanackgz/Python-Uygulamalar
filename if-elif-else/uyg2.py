# Bir kişinin doğum tarihini alarak,eğer yaşı 18 den büyük ise ehliyet
# alabilirsiniz,küçük ise ehliyet alamazsınız yazdıran programı kodlayın.

# Algoritma:

# 1-Başla
# 2-Bir değişken tanımla ve doğum tarihini al.
# 3-Yaşını hesapla.
# 4-Yaşını kontrol et--->
# 4.1 Eğer yaşı 18 den büyük ise ehliyet alabilirsin yaz.
# 4.2 Eğer yaşı 18 den küçük ise ehliyet alamazsın yaz.
# 5- Bitir

dogtar=int(input("Doğum tarihini giriniz="))
yas=2021-dogtar
if yas>=18:
    print("Ehliyet alabilirsiniz.")
else:
    print("Yaşınız ehliyet almaya uygun değildir.")
    
        



